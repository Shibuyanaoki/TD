#include "GameScene.h"
#include "TextureManager.h"
#include <cassert>

GameScene::GameScene() {}

GameScene::~GameScene() {}

void GameScene::Initialize() {

	dxCommon_ = DirectXCommon::GetInstance();
	input_ = Input::GetInstance();
	audio_ = Audio::GetInstance();

	//ワールドトランスフォームの初期化
	worldTransform_.Initialize();
	//ビューポートプロジェクションの初期化
	viewProjection_.Initialize();
	//プレイヤーの生成
	hammer_ = new Hammer;
	//敵の生成
	enemy_ = new Enemy;
	//ハンマーの初期化
	hammer_->Initialize();
	//敵の初期化
	enemy_->Initialize();

}

void GameScene::Update() {
	//ハンマーの更新
	hammer_->Updata();
	//敵の更新
	enemy_->Updata();

}

